/*
/ OPERATORS
An operator is a symbol that tells the compiler to perform specific mathematical or logical functions. 
Operators are symbols that triggers an action when applied to C variables and other objects. 
The data items on which operators act upon are called operands.
Depending on the number of operands that an operator can act upon, operators can be classified as follows:
//------------------------------------------------------------------------------------------------------------------------

. i) Unary Operators: 
Those operators that require only single operand to act upon are known as unary operators.For Example increment and 
decrement operators
//------------------------------------------------------------------------------------------------------------------------

.ii) Binary Operators: 
Those operators that require two operands to act upon are called binary operators. 

Binary operators are classified into :

*(a) Arithmetic Operators
(+, -, *, /, %, post-increment, pre-increment, post-decrement, pre-decrement)

*(b) Relational Operators
(==, !=, >, <, >= & <=) (&&, || and !)

*(c) Logical Operators
( && , || , !) Logical AND, OR, NOT Operators

*(d) Assignment Operators
It is used to assign values. 
(=, +=, -=, *=, /=, %=, <<, >>=, &=, ^=, |= )	 

*(e) Bitwise Operators      
as name suggests it does bitwise manipulation 
<<(left shift), >>(right shift), ~(NOT), |(OR), &(AND), ^(XOR)

! For explanation of each operator, go to the links down below. 
//------------------------------------------------------------------------------------------------------------------------

.iii) Ternary operators:
Those operators which requires three operands to act upon. 
For Example Conditional operator( ? : ).

SYNTAX:   condition ? expression1 : expression2  

first the condition is evalualted if it is true expression is executed otherwise expression2 is executed.



*/

