/*
/ DATATYPES
In C++, data types are declarations for variables. This determines the type and size of data associated with variables.


.1) Primary or Primitive or Built-in
- Integer (int) 
- Floating Point (float) 
- Double Floating Point (double)
- Character (char)
- Boolean (bool)
- void  
--------------------------------------------------------------------------------------------------------------------------

.2) Derived 
The data-types that are derived from the primitive or built-in datatypes are referred to as Derived Data Types.

- Functions
- Arrays
- Pointer 
- Reference
--------------------------------------------------------------------------------------------------------------------------

.3) Abstract or User Defined
These data types are defined by user itself. Like, defining a class in C++ or a structure. 
C++ provides the following user-defined datatypes:

- Class
- Structure
- Union
- Enumeration(Enum)
- Typedef 
--------------------------------------------------------------------------------------------------------------------------

*/

